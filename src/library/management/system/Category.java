    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
String category = txtcategory.getText();
String status = txtstatus.getSelectedItem().toString();

try {
    // Prepare the SQL insert query, using backticks for column names with spaces
    String sql = "INSERT INTO category (`Category_Name`, `Status`) VALUES (?, ?)";

    pst = con.prepareStatement(sql);
    
    // Set the values for the placeholders in the SQL statement
    pst.setString(1, category);
    pst.setString(2, status);
    
    // Execute the update query
    int k = pst.executeUpdate();
    
    // Check if the insertion was successful
    if (k == 1) {
        JOptionPane.showMessageDialog(this, "Category Created");
        
        // Reset input fields
        txtcategory.setText("");
        txtstatus.setSelectedIndex(-1);
        txtcategory.requestFocus();
        Category_Load();
    } else {
        JOptionPane.showMessageDialog(this, "Error");
    }

} catch (SQLException ex) {
    Logger.getLogger(Category.class.getName()).log(Level.SEVERE, null, ex);
}


 
 
    }//GEN-LAST:event_jButton1ActionPerformed

