    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
    String name = txtmember.getText();
    String address = txtaddress.getText();
    String phone = txtphone.getText();
    
    

try {
    // Prepare the SQL insert query, using backticks for column names with spaces
    String sql = "INSERT INTO member (`name`, `address` , `phone`) VALUES (?, ? , ?)";

    pst = con.prepareStatement(sql);
    
 
    pst.setString(1, name);
    pst.setString(2, address);
     pst.setString(3, phone);
    
   
    int k = pst.executeUpdate();
    
    
    if (k == 1) {
        JOptionPane.showMessageDialog(this, "Member Created");
        
      txtmember.setText("");
      txtaddress.setText("");
      txtphone.setText("");
      txtmember.requestFocus();
      
      Member_Load();
       
    } else {
        JOptionPane.showMessageDialog(this, "Error");
    }

} catch (SQLException ex) {
    Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
}


 
 
    }//GEN-LAST:event_jButton1ActionPerformed

