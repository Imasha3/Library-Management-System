    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
                                    
    String bname = txtname.getText();
    CategoryItem citem = (CategoryItem) txtcategory.getSelectedItem();
    String author = txtauthor.getText();
    String publisher =txtpub.getText();

    String contents = txtcontent.getText();
    String pages = txtno.getText();
    String edition = txtedition.getText();

    try {
        // Prepare the SQL insert query
        String sql = "INSERT INTO book (bname, category, author, publisher, contents, pages, edition) VALUES (?, ?, ?, ?, ?, ?, ?)";

        pst = con.prepareStatement(sql);

        // Set the values for the SQL query
        pst.setString(1, bname);
        pst.setInt(2, citem.id);
        pst.setString(3, author);
        pst.setString(4, publisher);
        pst.setString(5, contents);
        pst.setString(6, pages);
        pst.setString(7, edition);

        int k = pst.executeUpdate();

        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Book Created");

            // Reset the form fields
            txtname.setText("");
            txtcategory.setSelectedIndex(-1);
            txtauthor.setText("");
            txtpub.setText("");
            txtcontent.setText("");
            txtno.setText("");
            txtedition.setText("");
            txtname.requestFocus();
            
            Book_Load();

        } else {
            JOptionPane.showMessageDialog(this, "Error");
        }

    } catch (SQLException ex) {
        Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
    }


 
 
    }//GEN-LAST:event_jButton1ActionPerformed

